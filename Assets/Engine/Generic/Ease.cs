          return (1.0f + t*t*t);

		case EaseType.cubicInOut_01: //*
          t /= d/2;

          if (t < 1)
            return 1.0f/2.0f*t*t*t;

          t -= 2;

          return 1.0f/2.0f*(t*t*t + 2);



		case EaseType.quartIn_01: //*
          t /= d;
          return t*t*t*t;

		case EaseType.quartOut_01: //*
          t /= d;
          t--;
          return (1.0f - t*t*t*t);

		case EaseType.quartInOut_01: //*

          t /= d/2;

          if(t < 1)
            return 1.0f/2.0f*t*t*t*t;

          t -= 2;

          return -1.0f/2.0f * (t*t*t*t - 2);



		case EaseType.quintIn_01: //*
          t /= d;
          return t*t*t*t*t;

		case EaseType.quintOut_01: //*
          t /= d;
          t--;
          return (1.0f + t*t*t*t*t);

		case EaseType.quintInOut_01: //*

          t /= d/2;

          if(t < 1)
            return 1.0f/2.0f*t*t*t*t*t;

          t -= 2;

          return 1.0f/2.0f*(t*t*t*t*t + 2);



		case EaseType.sineIn_01: //*
          return 1.0f - Mathf.Cos(t/d * (Mathf.PI/2.0f));

		case EaseType.sineOut_01: //*
          return Mathf.Sin(t/d * (Mathf.PI/2.0f));

		case EaseType.sineInOut_01: //*
          return -1.0f/2.0f * (Mathf.Cos(Mathf.PI*t/d) - 1);

        default:
          //undefined();
          return 0;
      }
    }
